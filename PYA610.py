# 請撰寫一程式，讓使用者輸入四週各三天的溫度，接著計算並輸出這四週的平均溫度及最高、最低溫度。
# 提示1：平均溫度輸出到小數點後第二位。
# 提示2：最高溫度及最低溫度的輸出，如為31時，則輸出31，如為31.1時，則輸出31.1
# =============================================================================
lst1=[]
for i in range(1,5):
    print('Week %d:'%(i))
    for j in range(1,4):        
        x = eval(input('Day %d:'%(j)))
        lst1.append(x)

print('Average: %.2f'%(sum(lst1)/len(lst1)))#要設定，顯示時字串和變數中間才會空一格
print('Highest:',max(lst1))#不設定時，顯示時字串和變數中間會空一格     
print('Lowest:',min(lst1))
